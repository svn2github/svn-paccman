    void updateDisplay(CalculatorParser.State currentState, StringBuffer operand_1, StringBuffer operand_2) {
        String sDisplay;
        switch (currentState) {

            case INIT:
                sDisplay = "0";
                break;
            case OPERAND_1:
            case OPERATOR_1:
            case EVALUATED:
            case OPERATOR_2:
                sDisplay = operand_1.toString();
                break;
            case OPERAND_2:
                sDisplay = operand_2.toString();
                break;
            default:
                sDisplay = "?";
        }
        currentDisplayEdt.setText(sDisplay);
    }
    
