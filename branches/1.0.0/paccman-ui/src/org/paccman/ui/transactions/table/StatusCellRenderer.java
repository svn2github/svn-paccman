    public java.awt.Component getTableCellRendererComponent(javax.swing.JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
        
        java.awt.Component retValue;
        
        TransactionBase.ReconciliationState state = (TransactionBase.ReconciliationState)value;
        switch (state) {
            
            case RECONCILED:
                setIcon(new javax.swing.ImageIcon(getClass().getResource("/org/paccman/ui/resources/images/reconciled_reconciled.png")));
                break;
                
            case MARKED:
                setIcon(new javax.swing.ImageIcon(getClass().getResource("/org/paccman/ui/resources/images/reconciled_marked.png")));
                break;
                
            default:
                setIcon(new javax.swing.ImageIcon(getClass().getResource("/org/paccman/ui/resources/images/reconciled_normal.png")));
                break;
                
        }
        
        retValue = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
        setText("");
        
        return retValue;

    }

    
